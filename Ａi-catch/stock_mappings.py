def get_stock_name(symbol: str) -> str:
    """獲取股票中文名稱，若無對應則返回代碼本身"""
    clean_symbol = symbol.replace('.TW', '').replace('.TWO', '')
    return SYMBOL_NAME_MAP.get(clean_symbol, symbol)

def get_market_suffix(symbol: str) -> str:
    """判斷市場後綴 (暫時簡化版)"""
    # 這裡可以擴展更完整的上市櫃清單
    clean_symbol = symbol.replace('.TW', '').replace('.TWO', '')
    otc_list = ["3363", "8046", "8155", "5475", "6446", "3661"] # 範例
    return ".TWO" if clean_symbol in otc_list else ".TW"
